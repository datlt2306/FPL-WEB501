// function name - function declaration
function sum(a, b) {
    return a + b;
}
// excute function
console.log("Function name: ", sum(10, 20));

//  function expression
const sum2 = function (a, b) {
    return a + b;
};
console.log("Function Expression:", sum2(10, 20));

//  arrow function
const sum3 = (a, b) => a + b;
console.log("Arrow function:", sum3(10, 20));

